const mongoose = require('mongoose');

const PredictionSchema = new mongoose.Schema({
  // DONNÉES DES CAPTEURS (seules données utilisées)
  temperature: Number,          // Température ambiante
  humidity: Number,            // Humidité ambiante
  soilMoisture: Number,        // Humidité du sol (raw value)
  soilMoisture_percent: Number, // Humidité du sol en %
  lightIntensity: Number,      // Intensité lumineuse
  
  // Références
  deviceAssignmentID: { type: mongoose.Schema.Types.ObjectId, ref: 'DeviceAssignment' },
  sensorReadingID: { type: mongoose.Schema.Types.ObjectId, ref: 'SensorData' },
  plantID: { type: mongoose.Schema.Types.ObjectId, ref: 'Plant' },
  userID: { type: mongoose.Schema.Types.ObjectId, ref: 'User' },
  DeviceID: String,
  
  // Résultats
  prediction_class: Number,
  prediction_label: String,
  recommendation: String,
  confidence_score: { type: Number, default: null },
  
  // Métadonnées
  createdAt: { type: Date, default: Date.now },
  isAutoGenerated: { type: Boolean, default: false },
  ml_api_version: String,
  notes: String
  
}, {
  timestamps: true
});

// Index pour performances
PredictionSchema.index({ plantID: 1, createdAt: -1 });
PredictionSchema.index({ userID: 1, createdAt: -1 });
PredictionSchema.index({ DeviceID: 1, createdAt: -1 });

module.exports = mongoose.model('Prediction', PredictionSchema);